#include <stdio.h>
#include <math.h>


/*-----------------------------------------------------------------------------
-	                            Defines
-----------------------------------------------------------------------------*/
#define TRUE 1


/*-----------------------------------------------------------------------------
-	                            Prototypes
-----------------------------------------------------------------------------*/
int numButtons(int x, int y, int z, int a) {
    
    int numPressed;
    
    numPressed = x + y + z + a;
    
    return numPressed;
    
}


/*-----------------------------------------------------------------------------
-							  Implementation
-----------------------------------------------------------------------------*/
int main(void) {

	int btri, bo, bx, bsq;

    while (TRUE) {
		
		scanf("%d,%d,%d,%d", &btri, &bo, &bx, &bsq);
        printf("Number of buttons pressed: %d\n", numButtons(btri, bo, bx, bsq));
        fflush(stdout);

    }

  return 0;
}

